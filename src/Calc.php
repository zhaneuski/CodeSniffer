<?php

namespace App;

class Calc
{
    public function sum($a, $b)
    {
        return $a + $b;
    }

    public function dif($a, $b)
    {
        return $a - $b;
    }

    public function div($a, $b)
    {
        return $a / $b;
    }

    public function mul($a, $b)
    {
        return $a * $b;
    }
}
